class DataLoader:
    """Loads and normalises browser history from a Google Takeout JSON."""

    def __init__(self, path: str):
        self.path = path

    def load(self) -> list[dict]:
        with open(self.path, "r", encoding="utf-8") as f:
            raw = json.load(f)

        entries = []
        seen = set()
        for item in raw.get("Browser History", []):
            title = item.get("title", "").strip()
            url   = item.get("url", "").strip()
            ts    = item.get("time_usec", 0)
            if not title or not ts:
                continue
            key = (title, ts)
            if key in seen:
                continue
            seen.add(key)
            dt = datetime.fromtimestamp(ts / 1_000_000)
            entries.append({
                "title":  title,
                "url":    url,
                "ts":     ts,
                "dt":     dt,
                "hour":   dt.hour,
                "dow":    dt.weekday(),   # 0=Mon
                "domain": self._extract_domain(url),
                "date":   dt.date(),
            })

        entries.sort(key=lambda x: x["ts"])
        return entries[: Config.MAX_ENTRIES]

    @staticmethod
    def _extract_domain(url: str) -> str:
        m = re.search(r"https?://(?:www\.)?([^/?\s]+)", url)
        return m.group(1).lower() if m else "unknown"


