class FeatureExtractor:
    """Derives behavioural features from normalised entries."""

    def __init__(self, entries: list[dict]):
        self.entries = entries

    # ── Interests ──────────────────────────────
    def extract_interests(self) -> dict:
        cat_counter = Counter()
        for e in self.entries:
            text = (e["title"] + " " + e["url"]).lower()
            for kw, cat in Config.CATEGORY_MAP.items():
                if kw in text:
                    cat_counter[cat] += 1
        return cat_counter

    # ── Suspicious activity ─────────────────────
    def extract_suspicious(self) -> dict:
        flagged = []
        total_score = 0
        keyword_hits = Counter()

        for e in self.entries:
            text = (e["title"] + " " + e["url"]).lower()
            hit_score = 0
            hit_kws   = []
            for kw, weight in Config.SUSPICIOUS_TAXONOMY.items():
                if kw in text:
                    hit_score   += weight
                    hit_kws.append(kw)
                    keyword_hits[kw] += 1
            if hit_score:
                total_score += hit_score
                flagged.append({**e, "score": hit_score, "keywords": hit_kws})

        return {
            "flagged":       flagged,
            "total_score":   total_score,
            "unique_count":  len(flagged),
            "keyword_hits":  keyword_hits,
        }

    # ── Temporal patterns ──────────────────────
    def extract_temporal(self) -> dict:
        hourly = Counter(e["hour"] for e in self.entries)
        daily  = Counter(e["dow"]  for e in self.entries)

        # Night owl: 00-04 UTC
        night = sum(v for h, v in hourly.items() if 0 <= h <= 4)
        total = len(self.entries) or 1
        night_pct = night / total

        peak_hour = hourly.most_common(1)[0][0] if hourly else 12
        peak_day  = ["Mon","Tue","Wed","Thu","Fri","Sat","Sun"][
                    daily.most_common(1)[0][0]] if daily else "Unknown"

        behavior = (
            "Night-owl"   if night_pct > 0.30 else
            "Late-evening" if peak_hour >= 20  else
            "Daytime"     if 8 <= peak_hour <= 17 else
            "Early-riser"
        )
        return {
            "hourly":     hourly,
            "daily":      daily,
            "night_pct":  night_pct,
            "peak_hour":  peak_hour,
            "peak_day":   peak_day,
            "behavior":   behavior,
        }

    # ── Domain frequency ───────────────────────
    def extract_domains(self) -> Counter:
        return Counter(e["domain"] for e in self.entries)

    # ── Interest clustering (TF-IDF + KMeans) ──
    def cluster_interests(self) -> list[list[str]]:
        titles = [e["title"] for e in self.entries if len(e["title"]) > 5]
        if len(titles) < Config.N_CLUSTERS * 2:
            return []
        vec = TfidfVectorizer(max_features=500, stop_words="english")
        X   = vec.fit_transform(titles)
        km  = KMeans(n_clusters=Config.N_CLUSTERS, random_state=42, n_init=10)
        km.fit(X)
        feature_names = vec.get_feature_names_out()
        clusters = []
        for center in km.cluster_centers_:
            top_idx = center.argsort()[-5:][::-1]
            clusters.append([feature_names[i] for i in top_idx])
        return clusters

    # ── Risk score ─────────────────────────────
    @staticmethod
    def compute_risk(susp: dict) -> tuple[str, int]:
        score = susp["total_score"]
        if score >= 15:  return "Critical", score
        if score >= 8:   return "High",     score
        if score >= 4:   return "Medium",   score
        if score >= 1:   return "Low",      score
        return "Minimal", score


